void FLASH_IRQHandler(void)          /* generated by CubeMX */
{
    traceISR_ENTER();
    flash_wake_up = pdFALSE;
    HAL_FLASH_IRQHandler();          /* will end up in the callbacks below */
    portYIELD_FROM_ISR(flash_wake_up);
}



