static SemaphoreHandle_t xFlashDoneSem;
BaseType_t flash_wake_up = pdFALSE;


enum {
    UNI_HAL_FLASH_LATENCY_TIMEOUT_MS = 100U,
};


typedef struct {
    bool dwt_ready;
    uint32_t start_tick;
    uint32_t timeout_tick;
    uint32_t start_ms;
    uint32_t timeout_ms;
} uni_hal_flash_wait_timeout_t;


typedef struct {
    uint32_t max_hz;
    uint32_t latency;
    uint32_t wrhighfreq;
} uni_hal_flash_latency_step_t;

